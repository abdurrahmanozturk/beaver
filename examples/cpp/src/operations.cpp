#include <iostream>

double multiply(double x,double y)
{
  return x*y;
}

int multiply(int x,int y)
{
  return x*y;
}

double add(double x,double y)
{
  return x+y;
}

int add(int x,int y)
{
  return x+y;
}

double subtract(double x,double y)
{
  return x-y;
}

int subtract(int x,int y)
{
  return x-y;
}

double divide(double x,double y)
{
  return x/y;
}

int divide(int x,int y)
{
  return x/y;
}
